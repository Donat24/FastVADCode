#MKDIR
if os.path.exists(STRUCUTRED_DIR):
    shutil.rmtree(STRUCUTRED_DIR)

os.mkdir(STRUCUTRED_DIR)
os.mkdir(BACKGROUND_NOISES_DIR)
os.mkdir(FOREGROUND_NOISES_DIR)

def get_dir(filepath):
    
    #Mapping
    mapping = {
        "pdsounds"      : "pdsounds",
        "UrbanSound"    : "UrbanSound",
        "MS-SNSD-noise" : "MS-SNSD-noise",
        "musan"         : "musan"
    }

    #Iter
    for path_part, dir in mapping.items():
        if path_part in filepath:
            return dir
    
    raise Exception("Dataset nicht erkannt")

#BACKGROUND_NOISES
for files in tqdm.tqdm(BACKGROUND_NOISES):
    for file in glob.glob(os.path.join(UNSTRUCUTRED_DIR, files)):
        
        #Nur Soundfiles
        if not any(file.lower().endswith(ending) for ending in ["wav", "mp3", "flac", "ogg", "aiff"]):
            continue
        
        #Check length
        if librosa.get_duration(path=file) < 5:
            print(f"Datei {file} kleiner als 5 Sekunden")
            continue

        #Dataset Dir
        dir_path = os.path.join(BACKGROUND_NOISES_DIR, get_dir(file))
        if not os.path.exists(dir_path):
            os.mkdir(dir_path)
        
        #Target
        target_filepath = os.path.join(dir_path, os.path.basename(file))

        #Copy
        shutil.copy(src=file, dst=target_filepath)


